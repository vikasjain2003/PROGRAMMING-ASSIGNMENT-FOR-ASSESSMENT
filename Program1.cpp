// Program: 1
// “Write a Program for use of predefined sizeof() function to display the data type”

#include <iostream>
using namespace std;

int main() {
  cout<<"integer size is "<<sizeof(int)<<" bytes"<<endl;
  cout<<"float size is "<<sizeof(float)<<" bytes"<<endl;
  cout<<"char size is "<<sizeof(char)<<" bytes"<<endl;
  cout<<"Double size is "<<sizeof(double)<<" bytes"<<endl;
   return 0;
}
